<h1>Descriptif du DEUST BC2E</h1>
	<p class="title">Diplôme d'Etudes Universitaire de Sciences et Technologies<br/>en<br/>Bureautique et Communication Electronique en Entreprise</p>
	<p>Le DEUST BC2E est un diplôme de premier cycle (niveau Bac + 2). La formation vise, à travers une connaissance approfondie des matériels et logiciels utilisés en bureautique, des techniques de gestion et de l'aspect communication, à montrer la synergie qui existe entre les dimensions primordiales de la vie en entreprise.</p>
	<p>La qualification acquise est une réponse aux besoins des entreprises en matière d'utilisation de l'outil informatique dans le domaine tant de la gestion et l'organisation de l'entreprise que de la communication.</p>
	<h2>Objectifs</h2>
		<p>L'objectif du DEUST BC2E est de former des techniciens supérieurs en informatique/gestion désireux de participer au développement des entreprises.</p>
		<p>Les principales activités visées sont l'informatique et le multimédia (webmaster, correspondant informatique, concepteur multimédia, gestionnaire de réseaux, développeur de bases de données, ...) ainsi que l'administration de l'entreprise (responsable administratif, comptable, ...)</p>
	<h2>Programme</h2>
		<p>Voici un bref récapitulatif des éléments étudiés durant cette formation :</p>
		<ul>
			<li>Matières générales : français, mathématiques, anglais</li>
			<li>Bureautique : utilisation de traitements de texte (tableaux, publipostage, calculs) et de tableurs (graphiques, macro commandes, bases de données, tableaux croisés dynamiques)</li>
			<li>Bases de données : création de tables, formulaires et états complexes, automatisation, conception</li>
			<li>Architecture et sytème : choix justifié et mise en oeuvre</li>
			<li>Programmation structurée : bases de la structure d'un programme, conception et développement</li>
			<li>Conception de logiciels : construction d'applications autour d'outils bureautiques</li>
			<li>Réseaux : réseaux locaux, client-serveur, planification et installation, administration</li>
			<li>Concepts Internet : accès aux services Internet et développement Web</li>
			<li>Méthodologie de conception de bases de données : modélisation conceptuelle, logique et physique</li>
			<li>Développement Internet : web dynamique (Javascript, DHTML), programmation (ASP, PHP), services et outils (FTP, moteurs de recherche)</li>
			<li>Réalisation d'applications : dévellopement suivant un dossier de conception détaillé</li>
			<li>Anglais : dialogue, rédaction de notes, sujets professionels</li>
			<li>Techniques d'expression : gestion de l'information (compte rendu, synthèse, rapports), prise de parole en public</li>
			<li>Environnement juridique : introduction, droit du travail et droit de l'informatique</li>
			<li>Comptabilité générale : logique des mécanismes et outils comptables, analyses de documents et limites</li>
			<li>Gestion : analyse financière, fiscalité d'entreprise, comptabilité analytique et gestion budgétaire</li>
			<li>Economie d'entreprise : conaissance de l'entreprise, stratégies et enquêtes</li>
			<li>Statistiques : collecte, organisation et structuration des données, utilisation du tableur et construction de graphiques</li>
			<li>Recherche opérationelle : résolution de problèmes, communication des solutions et prise de décision</li>
		</ul>
